import math

print(math.pow(3,2))
# 3의 2승

print(math.sqrt(25))
# 25의 제곱근

print(math.ceil(3.14))
# 올림

print(math.floor(3.78))
# 내림

print(math.trunc(3.14))
# 0을 향하여 내림

print(math.copysign(3.14, -3))
# 두 번째 인자의 부호를 첫 번째 인자의 부호에 적용

print(math.fabs(-3.14))
# 절댓값

print(math.factorial(5))
# 팩토리얼

print(math.gcd(6,8))